package tr.com.kepce.meal;

public class Subproduct {

    private String id;
    private String name;
    private float price;
    private int calories;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public float getPrice() {
        return price;
    }

    public int getCalories() {
        return calories;
    }
}
